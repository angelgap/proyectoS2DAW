package com.S2DAW.Proyecto.Vee.Vee.dto;

public class LoginRequest {

        private String nombre;
        private String pass;

        // Getters y setters
        public String getNombre() {
            return nombre;
        }

        public void setNombre(String nombre) {
            this.nombre = nombre;
        }

        public String getPass() {
            return pass;
        }

        public void setPass(String pass) {
            this.pass = pass;
        }

}
